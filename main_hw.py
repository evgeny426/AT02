def count_russian_vowels(s: str) -> int:
    """
    Функция для подсчета количества гласных букв русского алфавита в строке.
    :param s: Входная строка.
    :return: Количество гласных букв русского алфавита в строке.
    """
    russian_vowels = "аеёиоуыэюяАЕЁИОУЫЭЮЯ"  # Все гласные буквы русского алфавита (в нижнем и верхнем регистре)
    return sum(1 for char in s if char in russian_vowels)